public class SwitchStatement {
    public static void main(String[] args) {
        String age = "Tuesday";
        switch (age) {
            case "Sunday":
                System.out.println("Sunday");
                break;
            case "Monday":
                System.out.println("Monday");
                break;
            case "Tuesday":
                System.out.println("Tuesday");
                break;
// ... (other cases)
            default:
                System.out.println("Invalid day");
}
    }
}
