public class ConditionalStatement2 {
    public static void main(String[] args) {
        int score = 85;
        if (score >= 90) {
            System.out.println("Excellent!");
}       else if (score >= 80) {
            System.out.println("Good!");
}       else if (score >= 70) {
            System.out.println("Average.");
}       else {
            System.out.println("Needs improvement");
}
    }
}


